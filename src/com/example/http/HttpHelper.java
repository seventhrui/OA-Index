package com.example.http;

import java.io.BufferedReader;
import java.io.DataOutputStream;
import java.io.File;
import java.io.FileInputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.net.HttpURLConnection;
import java.net.MalformedURLException;
import java.net.URL;
import java.util.UUID;

import com.example.utils.StringUtils;

import android.util.Log;

/**
 * 
 */
public class HttpHelper {
	private static final String TAG = "uploadFile";
    private static final int TIME_OUT = 10*1000;   //超时时间
    private static final String CHARSET = "utf-8"; //设置编码
    
    private static String urlPath;
	public HttpHelper() {

	}

	public HttpHelper(String urlPath) {
		this.urlPath = urlPath;
	}

	/**
	 * doget
	 * 
	 * @return String
	 * @throws Exception
	 */
	public String doGetString() throws Exception {
		String result = "";
		HttpURLConnection conn = (HttpURLConnection) new URL(urlPath)
				.openConnection();
		conn.setRequestMethod("GET");
		conn.setRequestProperty("Charset", "UTF-8");
		// 设置连接超时
		conn.setConnectTimeout(5 * 1000);
		conn.connect();
		int fileLength = conn.getContentLength();
		if (conn.getResponseCode() == 200 && fileLength > 0) {
			InputStreamReader inputStreamReader = new InputStreamReader(
					conn.getInputStream(), "UTF-8");
			BufferedReader reader = new BufferedReader(inputStreamReader);
			String data1 = null;
			StringBuffer stringBuffer = new StringBuffer();
			while ((data1 = reader.readLine()) != null) {
				stringBuffer.append(data1);
			}
			result = stringBuffer.toString();
		}
		return result;
	}

	/**
	 * dopost
	 * 
	 * @param poststring
	 * @return String
	 * @throws Exception
	 */
	public String doPostString(String poststring) throws Exception {
		String result = "";
		URL url = new URL(urlPath);
		HttpURLConnection conn = (HttpURLConnection) url.openConnection();
		conn.setConnectTimeout(5 * 1000);
		conn.setRequestMethod("POST");
		conn.setRequestProperty("Content-Type",
				"application/x-www-form-urlencoded");
		String content = poststring;
		byte[] data = content.getBytes();
		conn.setRequestProperty("Content-Length", data.length + "");
		conn.setDoOutput(true);
		conn.getOutputStream().write(data);

		if (conn.getResponseCode() == 200) {
			InputStream is = conn.getInputStream();
			byte[] resultdata = StringUtils.getStreamBytes(is);
			Log.i("----resultdata-----", new String(resultdata));
			result = new String(resultdata);
			Log.i("----redata-----", result + "LLLLLLLLLLL");
		} else
			result = conn.getResponseCode() + "";
		return result;
	}

	public static String uploadFile(File file) {
		String result = null;
		String BOUNDARY = UUID.randomUUID().toString(); // 边界标识 随机生成
		String PREFIX = "--", LINE_END = "\r\n";
		String CONTENT_TYPE = "multipart/form-data"; // 内容类型

		try {
			URL url = new URL(urlPath);
			HttpURLConnection conn = (HttpURLConnection) url.openConnection();
			conn.setReadTimeout(10 * 1000);
			conn.setConnectTimeout(10 * 1000);
			conn.setDoInput(true); // 允许输入流
			conn.setDoOutput(true); // 允许输出流
			conn.setUseCaches(false); // 不允许使用缓存
			conn.setRequestMethod("POST"); // 请求方式
			conn.setRequestProperty("Charset", "utf-8"); // 设置编码
			conn.setRequestProperty("connection", "keep-alive");
			conn.setRequestProperty("Content-Type", CONTENT_TYPE + ";boundary="
					+ BOUNDARY);

			if (file != null) {
				/**
				 * 当文件不为空，把文件包装并且上传
				 */
				DataOutputStream dos = new DataOutputStream(conn.getOutputStream());
				StringBuffer sb = new StringBuffer();
				sb.append(PREFIX);
				sb.append(BOUNDARY);
				sb.append(LINE_END);
				/**
				 * 这里重点注意： name里面的值为服务器端需要key 只有这个key 才可以得到对应的文件
				 * filename是文件的名字，包含后缀名的 比如:abc.png
				 */
				sb.append("Content-Disposition: form-data; name=\"file\"; filename=\""
						+ file.getName() + "\"" + LINE_END);
				sb.append("Content-Type: application/octet-stream; charset="
						+ CHARSET + LINE_END);
				sb.append(LINE_END);
				dos.write(sb.toString().getBytes());
				InputStream is = new FileInputStream(file);
				byte[] bytes = new byte[1024];
				int len = 0;
				while ((len = is.read(bytes)) != -1) {
					dos.write(bytes, 0, len);
				}
				is.close();
				dos.write(LINE_END.getBytes());
				byte[] end_data = (PREFIX + BOUNDARY + PREFIX + LINE_END)
						.getBytes();
				dos.write(end_data);
				dos.flush();
				/**
				 * 获取响应码 200=成功 当响应成功，获取响应的流
				 */
				int res = conn.getResponseCode();
				Log.e(TAG, "response code:" + res);
				Log.e(TAG, "request success");
				InputStream input = conn.getInputStream();
				StringBuffer sb1 = new StringBuffer();
				int ss;
				while ((ss = input.read()) != -1) {
					sb1.append((char) ss);
				}
				result = sb1.toString();
				Log.e(TAG, "result : " + result);
			}
		} catch (MalformedURLException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
		return result;
	}

	public String getUrlPath() {
		return urlPath;
	}

	public void setUrlPath(String urlPath) {
		this.urlPath = urlPath;
	}
}
